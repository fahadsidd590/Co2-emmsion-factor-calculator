<div class="row">
    <div class="col-md-10 col-12">
        <p>
            The Climate Compass uses the calculation method defined in the Greenhouse Gas Protocol - Corporate Accounting and Reporting Standard
            . The GHG protocol divides all emissions into three scopes. Scope 1 are direct emissions associated with burning for energy or
            transport purposes. Scope 2 are indirect emissions associated with energy consumption (electricity and district heating). Scope 3
            is indirect emissions associated with the purchase of, for example, products, materials, transport and services. The total
            discharge will be the sum of scope 1, 2 and 3.
        </p>
        <br>
        <p>
            Scope 1</p>
        <p>
            Includes all direct emission sources. The company's emissions are here related to the burning of fossil fuels at production sites
            and in means of transport owned, leased or leased by the company. Greenhouse gases related to chemical reactions in industrial
            processes (process-related emissions) or emissions from evaporation or emissions of greenhouse gases (volatile emissions) are
            also part of the direct emissions and are therefore part of scope 1.
        </p>
        <br>
        <p>
            Scope 2</p>
        <p>
            Scope 2 covers emissions from the companies that supply companies with collective energy. That is, greenhouse gases that originate
            from the production of electricity and district heating that the company consumes.
        </p>
        <br>
        <p>
            Scope 3</p>
        <p>
            Scope 3 covers all the indirect emissions not mentioned under scope 2. These can be, for example: subcontractors, use of products, waste treatment
            (disposal, recycling, incineration), transport of goods, transmission and distribution losses from electrical activities and the use phase of the product
        </p>
        <br>
        <p>
            Outside Scopes</p>
        <p>
            Biogenic CO2 emissions from the combustion of biofuels (eg biogas, bioethanol, biodiesel) are calculated outside Scope 1, 2, and 3
            . Possibly. avoided emissions (eg from avoided virgin production by sending waste for recycling or recycling) are also calculated
            outside Scopes 1, 2, and 3.
        </p>
        <br>
    </div>
</div>
