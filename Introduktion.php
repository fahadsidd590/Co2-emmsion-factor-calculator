        <p>
            The emissions are calculated based on a simple formula:
        </p>
        <br>
        <p>
            Emissions = Consumption * Emission coefficient
        </p>
        <p>
            Consumption here covers energy, fuels, purchasing and waste production, and is stated with a number of different units (eg kWh, L, DKK, kg or pcs).
        </p>
        <br>

        <p>
            The emission coefficients include all types of greenhouse gases and the calculated emissions will thus be in CO2 equivalents. The emission coefficients used in the Climate Compass are based on official national data sources (such as the Danish Energy Agency's Energy Statistics 2018) and internationally recognized models, which take into account all upstream emissions (such as models EXIOBASE).
            In some places it is possible to specify own emission factors in addition to the otherwise used emission factors.
        </p>
